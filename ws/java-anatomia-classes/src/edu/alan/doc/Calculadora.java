	/**
	 * Esse metodo soma dois numeros inteiros
	 * @param numeroUm esse é o primero numero
	 * @param numeroDois esse é o segundo numero
	 * @return o resultado da soma é um numero inteiro
	 */
	public int somar(int numeroUm, int numeroDois) {
		return numeroUm + numeroDois;
	}
	
